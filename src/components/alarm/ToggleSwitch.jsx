// src/components/alarm/ToggleSwitch.jsx
import React from "react";

const ToggleSwitch = ({ checked, onChange, disabled = false, size = "md" }) => {
  const sizes = {
    sm: { track: "w-8 h-4", thumb: "w-3 h-3", translate: "translate-x-4" },
    md: { track: "w-11 h-6", thumb: "w-5 h-5", translate: "translate-x-5" },
    lg: { track: "w-14 h-7", thumb: "w-6 h-6", translate: "translate-x-7" },
  };

  const s = sizes[size];

  return (
    <button
      type="button"
      role="switch"
      aria-checked={checked}
      disabled={disabled}
      onClick={() => onChange(!checked)}
      className={`relative inline-flex items-center rounded-full transition-colors duration-300 focus:outline-none focus:ring-2 focus:ring-blue-500 focus:ring-offset-2 disabled:opacity-50 disabled:cursor-not-allowed ${
        s.track
      } ${checked ? "bg-blue-600" : "bg-slate-600 dark:bg-gray-300"}`}
    >
      <span
        className={`inline-block rounded-full bg-white shadow-lg transform transition-transform duration-300 ${
          s.thumb
        } ${checked ? s.translate : "translate-x-0.5"}`}
      />
    </button>
  );
};

export default React.memo(ToggleSwitch);